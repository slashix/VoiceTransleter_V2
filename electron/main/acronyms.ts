/**
 * Словарь произношения технических акронимов для XTTS v2.
 *
 * XTTS нестабильно произносит акронимы как единое слово (например, "BGP"
 * иногда звучит как "BGP-P" или искажается) — задокументированное
 * ограничение модели. Разбивка по буквам ("Б Г П") тоже не подходит —
 * звучит как "би джи пи" вместо естественного слитного "биджипи".
 *
 * Решение: заменяем акроним на то, как его реально произносят вслух
 * (слитная транслитерация, без пауз/дефисов между буквами).
 *
 * Как добавить новый акроним:
 *   1. Найди в этом файле подходящее место (по алфавиту)
 *   2. Добавь строку: АББРЕВИАТУРА: 'транслитерация',
 *   3. Ключ — точное написание акронима заглавными латинскими буквами,
 *      как он встречается в тексте (2-6 символов).
 *   4. Значение — как это звучит вслух по-русски, слитно, без заглавных
 *      букв и знаков препинания внутри (чтобы XTTS не пытался это
 *      "прочитать" как акроним ещё раз).
 *
 * Если акронима нет в словаре — он остаётся в тексте как есть, без замены
 * (пайплайн не ломается, просто произношение может быть неидеальным).
 */
export const ACRONYM_PRONUNCIATION: Record<string, string> = {
  API: 'эйпиай',
  ARP: 'арп',
  BGP: 'биджипи',
  DCI: 'диси ай',
  DNS: 'диэнэс',
  ECMP: 'исиэмпи',
  EVPN: 'ивипиэн',
  MPLS: 'эмпиэлэс',
  NAT: 'нат',
  OSPF: 'оэспиэф',
  PFC: 'пиэфси',
  QFX: 'кьюэфикс',
  RR: 'эрар',
  SSH: 'эсэсэйч',
  TCP: 'тисипи',
  UDP: 'юдипи',
  VLAN: 'вилан',
  VRF: 'виарэф',
  VXLAN: 'виикслан',
}

/**
 * Заменяет известные акронимы в тексте на их произношение.
 * Акронимы, отсутствующие в словаре, остаются без изменений.
 */
export function applyAcronymPronunciation(text: string): string {
  return text.replace(/\b[A-Z]{2,6}\b/g, (match) => ACRONYM_PRONUNCIATION[match] ?? match)
}
